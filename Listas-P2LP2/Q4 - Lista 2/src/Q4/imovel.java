package Q4;

public class imovel {
    private String enredeço;
    private float preco;

    public imovel(String enredeço, float preco) {
        this.enredeço = enredeço;
        this.preco = preco;
    }

    public String getEnredeço() {
        return enredeço;
    }

    public void setEnredeço(String enredeço) {
        this.enredeço = enredeço;
    }

    public float getPreco() {
        return preco;
    }

    public void setPreco(float preco) {
        this.preco = preco;
    }
    
    
}
